const LETTER_HEIGHT = 400;
const Y_POSITION = 767;

export default function HeroLogo({ fill = '#ffffff', width = '80vw', maxWidth = '1200px' }) {
  // Custom gaps to achieve consistent visual kerning, matching the V-E gap.
  // The physical minimum gaps must vary because different letter shapes 
  // (slanted vs straight vs round) create different amounts of whitespace.
  const GAPS = [
    10, // V (/) to E (|)
    12, // E (|) to S (round)
    12, // S (round) to Y (\)
    10, // Y (/) to I (|) - same shape relationship as V to E
    15, // I (|) to N (|) - straight to straight needs a large gap to match the visual space
    15  // N (|) to E (|) - straight to straight
  ];
  
  const totalGap = GAPS.reduce((a, b) => a + b, 0);
  let curX = 373 - (totalGap / 2);
  const positionedLetters = LETTERS.map((letter, idx) => {
    const x = curX;
    const gapAfter = idx < GAPS.length ? GAPS[idx] : 0;
    curX += letter.width + gapAfter;
    return { ...letter, x };
  });

  return (
    <div style={{ width, maxWidth, margin: '0 auto' }}>
      <svg aria-hidden="true"
        viewBox="0 0 3440 1935"
        xmlns="http://www.w3.org/2000/svg"
        style={{ width: '100%', height: 'auto', display: 'block' }}
      >
        {positionedLetters.map((letter, idx) => (
          <svg
            key={idx}
            className={`vesline-char${idx === positionedLetters.length - 1 ? ' vesline-last-e' : ''}`}
            x={letter.x}
            y={letter.char === 's' ? Y_POSITION - 6 : Y_POSITION} // adjust s slightly because it rises higher
            width={letter.width}
            height={letter.char === 's' ? LETTER_HEIGHT + 14 : LETTER_HEIGHT}
            viewBox={letter.viewBox}
            overflow="visible"
          >
            <path
              d={letter.d}
              fill={fill}
              fillRule="evenodd"
              clipRule="evenodd"
            />
          </svg>
        ))}
      </svg>
    </div>
  );
}
